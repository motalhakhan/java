class ternary_op 
{
    public static void main(String[] args) 
    {
        int a = 16;
        int b = 9;
        // ternary operator ?: = if the statement is true run the code after ? if false then run the code after :
        // 10 for even 20 for odd

        boolean result = (a>b) ? true : false;
        System.out.println(result);
    }
}
